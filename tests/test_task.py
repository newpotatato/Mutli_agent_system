def test_task_classification():
    """–¢–µ—Å—Ç–∏—Ä–æ–≤–∞–Ω–∏–µ –∫–ª–∞—Å—Å–∏—Ñ–∏–∫–∞—Ü–∏–∏ –∑–∞–¥–∞—á —Å –¥–µ—Ç–∞–ª—å–Ω–æ–π –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏–µ–π"""
    print("=== –¢–ï–°–¢–ò–†–û–í–ê–ù–ò–ï –ö–õ–ê–°–°–ò–§–ò–ö–ê–¶–ò–ò –ó–ê–î–ê–ß ===")
    print("–ö–∞–∂–¥–∞—è –∑–∞–¥–∞—á–∞ —Å–æ–¥–µ—Ä–∂–∏—Ç –∫–ª—é—á–µ–≤—ã–µ —Å–ª–æ–≤–∞ –¥–ª—è –ø—Ä–∞–≤–∏–ª—å–Ω–æ–π –∫–ª–∞—Å—Å–∏—Ñ–∏–∫–∞—Ü–∏–∏\n")
    
    # –°—á–µ—Ç—á–∏–∫–∏ –¥–ª—è —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫–∏
    type_stats = {}
    confidence_stats = []
    
    for i, (prompt, priority, complexity) in enumerate(test_cases, 1):
        # –°–æ–∑–¥–∞–µ–º –∑–∞–¥–∞—á—É –±–µ–∑ debug —Ä–µ–∂–∏–º–∞ –¥–ª—è —á–∏—Å—Ç–æ–≥–æ –≤—ã–≤–æ–¥–∞
        task = Task(prompt, priority, complexity, debug=False)
        
        # –û–±–Ω–æ–≤–ª—è–µ–º —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫—É
        if task.type not in type_stats:
            type_stats[task.type] = 0
        type_stats[task.type] += 1
        confidence_stats.append(task.get_confidence_score())
        
        # –í—ã–≤–æ–¥–∏–º –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—é –æ –∑–∞–¥–∞—á–µ
        print(f"[{i:2d}] –¢–∏–ø: {task.type.upper():12} | –£–≤–µ—Ä–µ–Ω–Ω–æ—Å—Ç—å: {task.get_confidence_score():.2f}")
        print(f"     –ó–∞–¥–∞—á–∞: {prompt}")
        print(f"     –ü—Ä–∏–æ—Ä–∏—Ç–µ—Ç: {priority}, –°–ª–æ–∂–Ω–æ—Å—Ç—å: {complexity}")
        
        # –ü–æ–∫–∞–∑—ã–≤–∞–µ–º –∞–ª—å—Ç–µ—Ä–Ω–∞—Ç–∏–≤–Ω—ã–µ –≤–∞—Ä–∏–∞–Ω—Ç—ã –ø—Ä–∏ –Ω–∏–∑–∫–æ–π —É–≤–µ—Ä–µ–Ω–Ω–æ—Å—Ç–∏
        if task.get_confidence_score() < 0.3:
            alternatives = task.suggest_alternative_types(3)
            print(f"     –ê–ª—å—Ç–µ—Ä–Ω–∞—Ç–∏–≤—ã: {', '.join([f'{t}({s:.2f})' for t, s in alternatives[:3]])}")
        
        print()
    
    # –í—ã–≤–æ–¥–∏–º —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫—É
    print("=" * 70)
    print("–°–¢–ê–¢–ò–°–¢–ò–ö–ê –ö–õ–ê–°–°–ò–§–ò–ö–ê–¶–ò–ò:")
    print(f"–í—Å–µ–≥–æ –∑–∞–¥–∞—á: {len(test_cases)}")
    print("\n–†–∞—Å–ø—Ä–µ–¥–µ–ª–µ–Ω–∏–µ –ø–æ —Ç–∏–ø–∞–º:")
    for task_type, count in sorted(type_stats.items()):
        percentage = (count / len(test_cases)) * 100
        print(f"  {task_type:12} - {count:2d} –∑–∞–¥–∞—á ({percentage:4.1f}%)")
    
    avg_confidence = sum(confidence_stats) / len(confidence_stats)
    print(f"\n–°—Ä–µ–¥–Ω—è—è —É–≤–µ—Ä–µ–Ω–Ω–æ—Å—Ç—å –∫–ª–∞—Å—Å–∏—Ñ–∏–∫–∞—Ü–∏–∏: {avg_confidence:.2f}")
    
    low_confidence_count = sum(1 for c in confidence_stats if c < 0.3)
    print(f"–ó–∞–¥–∞—á —Å –Ω–∏–∑–∫–æ–π —É–≤–µ—Ä–µ–Ω–Ω–æ—Å—Ç—å—é (< 0.3): {low_confidence_count}")
    
    print("=" * 70)

